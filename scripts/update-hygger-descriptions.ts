async function updateDescriptions() {
    console.log("\n๐ Updating HYGGER products descriptions and benefits...\n");

    const allProducts = await db.select().from(products).where(eq(products.brand, "HYGGER"));
    console.log(`Found ${allProducts.length} HYGGER products\n`);

    let updated = 0;

    for (const product of allProducts) {
        // Find matching update
        let matchedUpdate: ProductUpdate | null = null;

        for (const [key, update] of Object.entries(productUpdates)) {
            if (product.name.includes(key) || product.name.startsWith(key)) {
                matchedUpdate = update;
                break;
            }
        }

        if (matchedUpdate) {
            // Merge benefits into specifications
            const existingSpecs = (product.specifications as Record<string, any>) || {};
            const updatedSpecs = {
                ...existingSpecs,
                benefits: matchedUpdate.benefits,
            };

            await db.update(products)
                .set({
                    description: matchedUpdate.description,
                    specifications: updatedSpecs,
                    updatedAt: new Date(),
                })
                .where(eq(products.id, product.id));

            console.log(`โ Updated: ${product.name}`);
            updated++;
        } else {
            console.log(`โญ๏ธ  No match for: ${product.name}`);
        }
    }

    console.log(`\nโ Updated ${updated} products with descriptions and benefits`);
}

