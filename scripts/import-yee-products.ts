// USD to IQD conversion + 35% profit margin
const USD_TO_IQD = 1480;
const PROFIT_MARGIN = 1.35;

function usdToIqd(usd: number): string {
    return Math.round(usd * USD_TO_IQD * PROFIT_MARGIN).toString();
}

function generateSlug(name: string): string {
    return name.toLowerCase()
        .replace(/[^a-z0-9\s-]/g, '')
        .replace(/\s+/g, '-')
        .replace(/-+/g, '-')
        .replace(/(^-|-$)/g, '');
}

function scanImagesInFolder(folderName: string): string[] {
    const folderPath = join(process.cwd(), "client", "public", "images", "products", "yee", folderName);

    try {
        if (!statSync(folderPath).isDirectory()) {
            return [];
        }

        const files = readdirSync(folderPath);
        const imageFiles = files.filter(file =>
            /\.(jpg|jpeg|png|gif|webp|avif)$/i.test(file)
        );

        return imageFiles.map(file => `/images/products/yee/${encodeURIComponent(folderName)}/${encodeURIComponent(file)}`);
    } catch (error) {
        return [];
    }
}

