// Helper functions
export function getArticleBySlug(slug: string): BlogArticle | undefined {
    return blogArticles.find((article) => article.slug === slug);
}

export function getArticlesByCategory(category: BlogArticle["category"]): BlogArticle[] {
    return blogArticles.filter((article) => article.category === category);
}

export function getFeaturedArticles(): BlogArticle[] {
    return blogArticles.filter((article) => article.featured);
}

export function getRelatedArticles(currentSlug: string, limit = 3): BlogArticle[] {
    const current = getArticleBySlug(currentSlug);
    if (!current) return blogArticles.slice(0, limit);

    return blogArticles
        .filter((article) => article.slug !== currentSlug)
        .filter((article) =>
            article.category === current.category ||
            article.tags.some((tag) => current.tags.includes(tag))
        )
        .slice(0, limit);
}

export function searchArticles(query: string): BlogArticle[] {
    const lowerQuery = query.toLowerCase();
    return blogArticles.filter(
        (article) =>
            article.title.toLowerCase().includes(lowerQuery) ||
            article.excerpt.toLowerCase().includes(lowerQuery) ||
            article.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
    );
}
